import { LegalEntry } from '@/types';
import Papa from 'papaparse';

export async function loadLegalData(): Promise<LegalEntry[]> {
  const entries: LegalEntry[] = [];
  
  // Load both CSV files
  const files = ['Bar_Editorials_1.csv', 'Bar_Editorials_2.csv'];
  
  for (const file of files) {
    const response = await fetch(`/data/${file}`);
    const text = await response.text();
    
    Papa.parse(text, {
      header: true,
      skipEmptyLines: true,
      complete: (results) => {
        results.data.forEach((row: any, index: number) => {
          if (row.year && row.Question) {
            entries.push({
              id: `${file}-${index}`,
              year: parseInt(row.year),
              volume: parseInt(row.volume) || 1,
              fact: row.fact || '',
              question: row.Question || '',
              judgement: row.judgement || '',
              law: row.law || '',
              comment: row.comment || '',
            });
          }
        });
      },
    });
  }
  
  return entries;
}

